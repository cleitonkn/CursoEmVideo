a = input('Digite qualquer coisa: ')

print('A variavel a é do tipo primitivo',type(a))
print('A variavel a é do alfabeto ?',a.isalpha())
print('A variavel a é um número ?',a.isnumeric())
print('A variavel a é contém espaço ?',a.isspace())
print('A variavel a está em letra minuscula ?',a.islower())
print('A variavel a está em letra maiuscula ?',a.isupper())
